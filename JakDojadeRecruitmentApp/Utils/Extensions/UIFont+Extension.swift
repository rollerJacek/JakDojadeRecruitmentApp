import UIKit

extension UIFont {
    static func manropeBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Manrope-Bold", size: size)
    }
    static func manropeExtraBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Manrope-ExtraBold", size: size)
    }
    static func manropeExtraLight(size: CGFloat) -> UIFont? {
        return UIFont(name: "Manrope-ExtraLight", size: size)
    }
    static func manropeLight(size: CGFloat) -> UIFont? {
        return UIFont(name: "Manrope-Light", size: size)
    }
    static func manropeMedium(size: CGFloat) -> UIFont? {
        return UIFont(name: "Manrope-Medium", size: size)
    }
    static func manropeRegular(size: CGFloat) -> UIFont? {
        return UIFont(name: "Manrope-Regular", size: size)
    }
    static func manropeSemiBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Manrope-SemiBold", size: size)
    }
}
